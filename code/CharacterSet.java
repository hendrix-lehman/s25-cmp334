class CharacterSet {
  public static void main(String[] args) {
    char ch = 97;
    System.out.println("The character is: " + ch);

    long num = Long.MAX_VALUE;
    System.out.println("The maximum value of long is: " + num);
    num = num + 1;
    System.out.println("The maximum value of long is: " + num);
  }
}
